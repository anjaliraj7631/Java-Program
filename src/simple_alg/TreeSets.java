package simple_alg;

import java.util.TreeSet;

public class TreeSets {
    public static void main(String[] args) {
        TreeSet t=new TreeSet();
        t.add(6);
        t.add(21);
        t.add(3);
        t.add(7);
        t.add(5);
        System.out.println(t);
        // It will arrange all the element in ascending order
    }
}
